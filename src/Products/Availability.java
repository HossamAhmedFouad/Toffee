package Products;
/**
 * An enumeration representing the availability of a product. 
 */
public enum Availability {
    /**
     * Indicates that the product is currently on sale.
     */
    onSale,
    /**
     * Indicates that the product is not currently on sale.
     */
    notOnSale,
    /**
     * Indicates that the product is currently out of stock.
     */
    outOfStock
}